
DROP VIEW IF EXISTS WORLD.CAPITALS;
DROP VIEW IF EXISTS WORLD.CITIES;
DROP VIEW IF EXISTS WORLD.COUNTRIES;

CREATE VIEW WORLD.CAPITALS AS
SELECT COUNTRY.NAME as "country", CITY.NAME as "capital"
FROM WORLD.COUNTRY
    JOIN WORLD.CITY ON COUNTRY.CAPITAL = CITY.ID;

CREATE VIEW WORLD.CITIES AS
SELECT *
FROM WORLD.CITY
ORDER BY NAME
LIMIT 20;

CREATE VIEW WORLD.COUNTRIES AS
SELECT *
FROM WORLD.COUNTRY
ORDER BY NAME
LIMIT 20;
